"""
元组：
1、元组的定义：使用（）来表示

2、元组中可以存放任何的数据类型

3、元组中的元素是有序的：
    支持索引取值
    支持切片
4、元组中的元素是不可变的(定义了之后，元组内部的元素不能进行添加、删除和修改)

5、元组的查找：
    1、通过索引取值
    2、index:查找指定元素的索引
    3、count：统计某个元素在元组中的个数

"""
li = ['musen', 18, 'nan']  # 列表

tu = ('musen', 18, 'nan')  # 元组

print(tu[0])

print(tu[1:])

print(tu.index(18))
print(tu.count('musen'))
